package com.my.iud.dto;

public class Message {
	
	public boolean status;
	
	public String rs;

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}

	public String getRs() {
		return rs;
	}

	public void setRs(String rs) {
		this.rs = rs;
	}
    
}
